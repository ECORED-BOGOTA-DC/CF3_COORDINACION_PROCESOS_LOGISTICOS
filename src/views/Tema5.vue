      h2 5.1 Respuesta eficiente al cliente (ECR)
    p.mb-4 ECR (Respuesta Eficiente al Consumidor – Efficient Consumer Response) es un modelo estratégico de negocios, en el cual, proveedores y distribuidores trabajan muy cerca para llevar mejores productos al consumidor, enfocándose conjuntamente en la eficiencia total del sistema de abastecimiento, en lugar de la eficiencia individual del productor o fabricante, logrando la reducción de costos de toda la cadena, en los inventarios y aumentando las ventajas físicas de la distribución, mientras el consumidor recibe productos de alta calidad.
    .cajon.color-primario.p-4.mb-5
      p.mb-0 El objetivo final de ECR consiste en integrar los procesos logísticos y comerciales, pasando de un esquema push, donde los industriales fuerzan el ingreso de los productos a los canales comerciales, a un esquema pull, en el cual, se responde a la demanda real del consumidor logrando así una maximización de su satisfacción al mismo tiempo que se reducen los costos totales de operación.
